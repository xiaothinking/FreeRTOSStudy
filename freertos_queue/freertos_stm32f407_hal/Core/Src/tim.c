void delay_us(uint32_t Delay_us)
{
	__HAL_TIM_SetCounter(&htim3,0);
	__HAL_TIM_ENABLE(&htim3);
	while(__HAL_TIM_GetCounter(&htim3)<Delay_us);
	__HAL_TIM_DISABLE(&htim3);
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	uint8_t *buffer;
	BaseType_t xTaskWokenByReceive=pdFALSE;
	BaseType_t err;
	if(htim->Instance==TIM9)
   {
		buffer=mymalloc(SRAMIN,USART_REC_LEN);
        if(myQueue02Handle!=NULL)
        {
			memset(buffer,0,USART_REC_LEN);	//清除缓冲区
			err=xQueueReceiveFromISR(myQueue02Handle,buffer,&xTaskWokenByReceive);//请求消息Message_Queue
            if(err==pdTRUE)			//接收到消息
            {
				disp_str(buffer);	//在LCD上显示接收到的消息
            }
        }
		myfree(SRAMIN,buffer);		//释放内存
		
		portYIELD_FROM_ISR(xTaskWokenByReceive);//如果需要的话进行一次任务切换
	 }
}
